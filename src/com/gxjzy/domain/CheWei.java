package com.gxjzy.domain;

public class CheWei {

    private int id;
    private String name;
    private String time;
    private String weiname;
    private String weiphone;
    private String remark;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getRemark() {
        return remark;
    }

    public void setRemark(String remark) {
        this.remark = remark;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getWeiname() {
        return weiname;
    }

    public void setWeiname(String weiname) {
        this.weiname = weiname;
    }

    public String getWeiphone() {
        return weiphone;
    }

    public void setWeiphone(String weiphone) {
        this.weiphone = weiphone;
    }
}
