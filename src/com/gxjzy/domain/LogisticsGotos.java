package com.gxjzy.domain;

public class LogisticsGotos {

    private int id;
    private String name;
    private String carname;
    private String peiname;
    private int peiphone;
    private String time;
    private String remark;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getCarname() {
        return carname;
    }

    public void setCarname(String carname) {
        this.carname = carname;
    }

    public String getPeiname() {
        return peiname;
    }

    public void setPeiname(String peiname) {
        this.peiname = peiname;
    }

    public int getPeiphone() {
        return peiphone;
    }

    public void setPeiphone(int peiphone) {
        this.peiphone = peiphone;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getRemark() {
        return remark;
    }

    public void setRemark(String remark) {
        this.remark = remark;
    }
}
